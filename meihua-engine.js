let hexIdx = upIdx * 8 + dnIdx;
let name = GUA_NAMES[hexIdx];
// 体用
let isUpMoving = yaoIdx >= 4;
let tiIdx = isUpMoving ? dnIdx : upIdx;
let yongIdx = isUpMoving ? upIdx : dnIdx;
let tiWx = GUA_WUXING[tiIdx];
let yongWx = GUA_WUXING[yongIdx];
let shengKe = "";
var wxSheng = {"木":"火","火":"土","土":"金","金":"水","水":"木"};
